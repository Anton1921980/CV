        <p><strong>Существует несколько разных типов слуховых аппаратов.</strong></p>
        <p><strong>Внутриушные</strong>.<br />Слуховые аппараты,<strong><span>&nbsp;</span>которые носят в ухе</strong>, обычно изготовляются под заказ, основываясь на слепке уха. Они доступны в разных размерах для необходимого набора функций и в разных оттенках кожи для маскировки. Существует несколько стилей - каждый из них указан више, от минимального до самого большого. Стили: IIC, CIC, ITC, ITE</p>
        <p><strong>Заушные(BTE)<br /></strong>Модели расположены<span>&nbsp;</span><strong>за ухом с трубкой или проводкой</strong>, которая направляет звуки в ухо, которое соединяется с наконечником или ушным вкладышем для закрепления их в ушном канале. BTE выпускаются в различных цветах, чтобы сочетаться с волосами или тонами кожи и для личных стилей.<span>&nbsp;</span><strong>Различные размеры BTE поддерживают различные функции</strong>, элементы управления, типы батарей и степень мощности (более крупные приборы, как правило, имеют большую мощность, чем более мелкие).</p>
        <p><strong>Ресивер в канале (RIC):<br /></strong>RIC похож на BTE,<span>&nbsp;</span><strong>используя тонкую трубку</strong>. Однако вместо излучателя звука слухового аппарата, находящегося внутри корпуса BTE,<span>&nbsp;</span><strong>посылая звук через тонкую трубку</strong>, а затем в ушной канал, динамик находится внутри ушного канала, посылая усиленный звук прямо на барабанную перепонку.</p>
